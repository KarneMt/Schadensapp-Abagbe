        public MemoryStream GeneratePDF(PDFModel model)
        {
            // Create a new PDF document
            // Erstellen eines neuen MemoryStream für das PDF-Dokument
            MemoryStream memoryStream = new();
            PdfWriter writer = new(memoryStream);
            PdfDocument pdfDoc = new(writer);
            Document document = new(pdfDoc);

            // Setzen der Seitengröße auf A4
            PageSize pageSize = PageSize.A4;
            pdfDoc.SetDefaultPageSize(pageSize);

            // Generieren des Inhalts für die Vorlage
            StringBuilder sb = new();
            string template = GenerateTemplate(model);
            sb.Append(template);
            string htmlContent = sb.ToString();

            // Parsen des HTML-Inhalts und Hinzufügen zum Dokument
            using (MemoryStream htmlStream = new(Encoding.GetEncoding("windows-1252").GetBytes(htmlContent)))
            {
                // Die HtmlConverter-Klasse wird verwendet, um das HTML in das PDF zu konvertieren
                HtmlConverter.ConvertToPdf(htmlStream, pdfDoc);
            }
            // Schließen des Dokuments
            document.Close();

            // Erstellen einer Kopie des MemoryStream und Rückgabe
            MemoryStream memoryStreamCopy = new MemoryStream(memoryStream.ToArray());
            return memoryStreamCopy;
        }

